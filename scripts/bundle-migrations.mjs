#!/usr/bin/env node
// Bundle Supabase migrations into a single file you can paste into the
// Supabase SQL editor in one go (instead of applying them one by one).
//
// Usage:
//   node scripts/bundle-migrations.mjs            # bundle ALL migrations
//   node scripts/bundle-migrations.mjs 059        # bundle 059 and everything after
//   npm run db:bundle -- 059
//
// Output: supabase/apply-all.sql  (gitignored)
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const migrationsDir = path.join(__dirname, '..', 'supabase', 'migrations');
const outFile = path.join(__dirname, '..', 'supabase', 'apply-all.sql');

const from = process.argv[2] ? parseInt(process.argv[2], 10) : 0;

const files = fs.readdirSync(migrationsDir)
  .filter((f) => f.endsWith('.sql'))
  .sort()
  .filter((f) => {
    const n = parseInt(f.slice(0, 3), 10);
    return Number.isNaN(n) ? true : n >= from;
  });

if (files.length === 0) {
  console.log('No migrations matched.');
  process.exit(0);
}

const parts = [
  '-- ⚠️  Auto-generated by scripts/bundle-migrations.mjs — do not edit by hand.',
  `-- Bundles ${files.length} migration(s)${from ? ` from ${String(from).padStart(3, '0')} onward` : ''}.`,
  '-- Paste the whole file into the Supabase SQL editor and run once.',
  '',
];
for (const f of files) {
  const sql = fs.readFileSync(path.join(migrationsDir, f), 'utf8').trim();
  parts.push(`-- ======================================================================`);
  parts.push(`-- ${f}`);
  parts.push(`-- ======================================================================`);
  parts.push(sql, '', '');
}

fs.writeFileSync(outFile, parts.join('\n'));
console.log(`Bundled ${files.length} migration(s) into supabase/apply-all.sql:`);
files.forEach((f) => console.log(`  • ${f}`));
console.log('\nPaste supabase/apply-all.sql into the Supabase SQL editor and run it.');
